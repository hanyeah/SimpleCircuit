namespace SimpleCircuit.Contributions
{
    /// <summary>
    /// The types of unknowns.
    /// </summary>
    public enum UnknownTypes
    {
        /// <summary>
        /// The x-coordinate.
        /// </summary>
        X,

        /// <summary>
        /// The y-coordinate.
        /// </summary>
        Y,

        /// <summary>
        /// The angle.
        /// </summary>
        Angle,

        /// <summary>
        /// The length.
        /// </summary>
        Length,

        /// <summary>
        /// The scale along the x-axis.
        /// </summary>
        ScaleX,

        /// <summary>
        /// The scale along the y-axis.
        /// </summary>
        ScaleY,

        /// <summary>
        /// A scalar with no special meaning.
        /// </summary>
        Scalar
    }
}
